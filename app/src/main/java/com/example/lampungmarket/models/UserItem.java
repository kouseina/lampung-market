package com.example.lampungmarket.models;

public class UserItem {
    public String id;
    public String fullname;
    public String email;
    public String phone;

    public UserItem(String id, String fullname, String email, String phone) {
        this.id = id;
        this.fullname = fullname;
        this.email = email;
        this.phone = phone;
    }
}
